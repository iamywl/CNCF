// generateCRDYAML은 CRDSpec으로부터 CRD YAML을 생성한다.
func generateCRDYAML(spec *CRDSpec) string {
	tmpl, err := template.New("crd").Parse(CRDYAMLTemplate)
	if err != nil {
		panic(fmt.Sprintf("template parse error: %v", err))
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, spec); err != nil {
		panic(fmt.Sprintf("template execute error: %v", err))
	}
	return buf.String()
}

