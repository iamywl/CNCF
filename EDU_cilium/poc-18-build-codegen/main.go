// FieldKind는 구조체 필드의 종류를 나타낸다.
type FieldKind int

const (
	FieldPrimitive FieldKind = iota // int, string, bool 등
	FieldPointer                    // *T
	FieldSlice                      // []T
	FieldMap                        // map[K]V
	FieldStruct                     // 중첩 구조체
)

// StructField는 구조체의 필드 정보를 나타낸다.
type StructField struct {
	Name     string
	TypeName string
	Kind     FieldKind
	ElemType string // 슬라이스/맵의 원소 타입
	KeyType  string // 맵의 키 타입
}

// StructDef는 구조체 정의를 나타낸다.
type StructDef struct {
	Name        string
	Fields      []StructField
	Markers     []string // +k8s:deepcopy-gen 마커
	IsRuntimeObj bool    // runtime.Object 인터페이스 구현 여부
}

// DeepCopyTemplate은 DeepCopy 코드를 생성하는 템플릿이다.
const DeepCopyTemplate = `//go:build !ignore_autogenerated

// Code generated by deepcopy-gen. DO NOT EDIT.

package {{.PackageName}}

import (
	runtime "k8s.io/apimachinery/pkg/runtime"
)

{{range .Structs}}
// DeepCopyInto is an autogenerated deepcopy function, copying the receiver, writing into out. in must be non-nil.
func (in *{{.Name}}) DeepCopyInto(out *{{.Name}}) {
	*out = *in
{{- range .Fields}}
{{- if eq .Kind 1}} {{/* FieldPointer */}}
	if in.{{.Name}} != nil {
		in, out := &in.{{.Name}}, &out.{{.Name}}
		*out = new({{.ElemType}})
		{{- if eq .ElemType "string"}}
		**out = **in
		{{- else}}
		(*in).DeepCopyInto(*out)
		{{- end}}
	}
{{- else if eq .Kind 2}} {{/* FieldSlice */}}
	if in.{{.Name}} != nil {
		in, out := &in.{{.Name}}, &out.{{.Name}}
		*out = make([]{{.ElemType}}, len(*in))
		{{- if eq .ElemType "string"}}
		copy(*out, *in)
		{{- else}}
		for i := range *in {
			(*in)[i].DeepCopyInto(&(*out)[i])
		}
		{{- end}}
	}
{{- else if eq .Kind 3}} {{/* FieldMap */}}
	if in.{{.Name}} != nil {
		in, out := &in.{{.Name}}, &out.{{.Name}}
		*out = make(map[{{.KeyType}}]{{.ElemType}}, len(*in))
		for key, val := range *in {
			(*out)[key] = val
		}
	}
{{- else if eq .Kind 4}} {{/* FieldStruct */}}
	in.{{.Name}}.DeepCopyInto(&out.{{.Name}})
{{- end}}
{{- end}}
	return
}

// DeepCopy is an autogenerated deepcopy function, copying the receiver, creating a new {{.Name}}.
func (in *{{.Name}}) DeepCopy() *{{.Name}} {
	if in == nil {
		return nil
	}
	out := new({{.Name}})
	in.DeepCopyInto(out)
	return out
}
{{if .IsRuntimeObj}}
// DeepCopyObject is an autogenerated deepcopy function, copying the receiver, creating a new runtime.Object.
func (in *{{.Name}}) DeepCopyObject() runtime.Object {
	if c := in.DeepCopy(); c != nil {
		return c
	}
	return nil
}
{{end}}
{{end}}
`

// generateDeepCopyCode는 구조체 정의로부터 DeepCopy 코드를 생성한다.
func generateDeepCopyCode(packageName string, structs []StructDef) string {
	tmpl, err := template.New("deepcopy").Parse(DeepCopyTemplate)
	if err != nil {
		panic(fmt.Sprintf("template parse error: %v", err))
	}

	data := struct {
		PackageName string
		Structs     []StructDef
	}{
		PackageName: packageName,
		Structs:     structs,
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, data); err != nil {
		panic(fmt.Sprintf("template execute error: %v", err))
	}
	return buf.String()
}

