// 5. 증분 빌드 감지 시뮬레이션
// =============================================================================
// Makefile의 파일 수정 시간 기반 재빌드 판단 로직을 시뮬레이션한다.

// FileInfo는 빌드 파일의 정보를 나타낸다.
type FileInfo struct {
	Path     string
	ModTime  time.Time
	IsTarget bool // 빌드 산출물인지 여부
}

// IncrementalBuildChecker는 증분 빌드 필요 여부를 판단한다.
type IncrementalBuildChecker struct {
	Files map[string]*FileInfo
	Rules map[string][]string // target -> dependencies
}

// NewIncrementalBuildChecker는 새 증분 빌드 체커를 생성한다.
func NewIncrementalBuildChecker() *IncrementalBuildChecker {
	return &IncrementalBuildChecker{
		Files: make(map[string]*FileInfo),
		Rules: make(map[string][]string),
	}
}

// AddFile은 파일 정보를 등록한다.
func (c *IncrementalBuildChecker) AddFile(path string, modTime time.Time, isTarget bool) {
	c.Files[path] = &FileInfo{Path: path, ModTime: modTime, IsTarget: isTarget}
}

// AddRule은 빌드 규칙을 등록한다.
func (c *IncrementalBuildChecker) AddRule(target string, deps []string) {
	c.Rules[target] = deps
}

// NeedsRebuild는 대상 파일이 재빌드가 필요한지 판단한다.
func (c *IncrementalBuildChecker) NeedsRebuild(target string) (bool, string) {
	targetFile, ok := c.Files[target]
	if !ok {
		return true, "target does not exist"
	}

	deps, ok := c.Rules[target]
	if !ok {
		return false, "no dependencies defined"
	}

	for _, dep := range deps {
		depFile, ok := c.Files[dep]
		if !ok {
			return true, fmt.Sprintf("dependency '%s' not found", dep)
		}
		if depFile.ModTime.After(targetFile.ModTime) {
			return true, fmt.Sprintf("dependency '%s' is newer (%s > %s)",
				dep,
				depFile.ModTime.Format("15:04:05"),
				targetFile.ModTime.Format("15:04:05"),
			)
		}
	}

	return false, "all dependencies are older than target"
}

// CheckAll은 모든 타겟에 대해 재빌드 필요 여부를 확인한다.
func (c *IncrementalBuildChecker) CheckAll() map[string]bool {
	result := make(map[string]bool)
	for target := range c.Rules {
		needs, _ := c.NeedsRebuild(target)
		result[target] = needs
	}
	return result
}

