// =============================================================================
// 1. Protobuf 유사 코드 생성
// =============================================================================
// Cilium의 api/v1/flow/flow.proto, api/v1/observer/observer.proto를
// protoc로 컴파일하는 과정을 시뮬레이션한다.

// ProtoFieldType은 protobuf 필드 타입을 나타낸다.
type ProtoFieldType int

const (
	ProtoString ProtoFieldType = iota
	ProtoInt32
	ProtoInt64
	ProtoUint32
	ProtoUint64
	ProtoBool
	ProtoBytes
	ProtoTimestamp
	ProtoEnum
	ProtoMessage
	ProtoRepeatedString
	ProtoRepeatedMessage
)

// ProtoField는 protobuf 메시지의 필드를 나타낸다.
type ProtoField struct {
	Name       string
	Type       ProtoFieldType
	TypeName   string // ProtoMessage/ProtoEnum일 때 타입 이름
	FieldNum   int
	Deprecated bool
	Comment    string
}

// ProtoMessageDef는 protobuf 메시지 정의를 나타낸다.
type ProtoMessageDef struct {
	Name    string
	Fields  []ProtoField
	Package string
	Comment string
}

// ProtoEnumDef는 protobuf enum 정의를 나타낸다.
type ProtoEnumDef struct {
	Name   string
	Values []ProtoEnumValue
}

// ProtoEnumValue는 enum의 값을 나타낸다.
type ProtoEnumValue struct {
	Name  string
	Value int
}

// ProtoService는 gRPC 서비스 정의를 나타낸다.
type ProtoService struct {
	Name    string
	Methods []ProtoMethod
}

// ProtoMethod는 gRPC 메서드를 나타낸다.
type ProtoMethod struct {
	Name         string
	InputType    string
	OutputType   string
	ServerStream bool
	ClientStream bool
}

// ProtoFile은 하나의 .proto 파일을 나타낸다.
type ProtoFile struct {
	Package  string
	GoPackage string
	Messages []ProtoMessageDef
	Enums    []ProtoEnumDef
	Services []ProtoService
}

// protoTypeToGoType은 protobuf 타입을 Go 타입으로 변환한다.
func protoTypeToGoType(ft ProtoFieldType, typeName string) string {
	switch ft {
	case ProtoString:
		return "string"
	case ProtoInt32:
		return "int32"
	case ProtoInt64:
		return "int64"
	case ProtoUint32:
		return "uint32"
	case ProtoUint64:
		return "uint64"
	case ProtoBool:
		return "bool"
	case ProtoBytes:
		return "[]byte"
	case ProtoTimestamp:
		return "time.Time"
	case ProtoEnum:
		return typeName
	case ProtoMessage:
		return "*" + typeName
	case ProtoRepeatedString:
		return "[]string"
	case ProtoRepeatedMessage:
		return "[]*" + typeName
	default:
		return "interface{}"
	}
}

// GoStructTemplate은 protobuf 메시지에서 Go 구조체를 생성하는 템플릿이다.
const GoStructTemplate = `// Code generated by protoc-gen-go. DO NOT EDIT.
// source: {{.Package}}/{{toLower .Name}}.proto

package {{.Package}}

{{range .Enums}}
// {{.Name}} represents the protobuf enum {{.Name}}.
type {{.Name}} int32

const (
{{- range .Values}}
	{{$.Name}}_{{.Name}} {{$.Name}} = {{.Value}}
{{- end}}
)
{{end}}

{{range .Messages}}
// {{.Name}} represents the protobuf message {{.Name}}.
{{- if .Comment}}
// {{.Comment}}
{{- end}}
type {{.Name}} struct {
{{- range .Fields}}
{{- if .Deprecated}}
	// Deprecated: {{.Comment}}
{{- else if .Comment}}
	// {{.Comment}}
{{- end}}
	{{goFieldName .Name}} {{goType .Type .TypeName}} ` + "`" + `protobuf:"{{protoWireType .Type}},{{.FieldNum}},opt,name={{.Name}}" json:"{{.Name}},omitempty"` + "`" + `
{{- end}}
}

// Marshal serializes the {{.Name}} to bytes.
func (m *{{.Name}}) Marshal() ([]byte, error) {
	// 시뮬레이션: 실제로는 protobuf 인코딩 사용
	size := 0
{{- range .Fields}}
	_ = m.{{goFieldName .Name}} // field {{.FieldNum}}
	size += {{.FieldNum}} + 1
{{- end}}
	data := make([]byte, size)
	return data, nil
}

// Unmarshal deserializes bytes into {{.Name}}.
func (m *{{.Name}}) Unmarshal(data []byte) error {
	// 시뮬레이션: 실제로는 protobuf 디코딩 사용
	_ = data
	return nil
}

// MarshalJSON implements custom JSON marshaling.
func (m *{{.Name}}) MarshalJSON() ([]byte, error) {
	// 시뮬레이션: protoc-gen-go-json 생성 코드
	return []byte("{}"), nil
}
{{end}}

{{range .Services}}
// {{.Name}}Server is the server API for {{.Name}} service.
type {{.Name}}Server interface {
{{- range .Methods}}
{{- if .ServerStream}}
	{{.Name}}(*{{.InputType}}, {{$.Name}}_{{.Name}}Server) error
{{- else}}
	{{.Name}}(*{{.InputType}}) (*{{.OutputType}}, error)
{{- end}}
{{- end}}
}

// {{.Name}}Client is the client API for {{.Name}} service.
type {{.Name}}Client interface {
{{- range .Methods}}
{{- if .ServerStream}}
	{{.Name}}(*{{.InputType}}) ({{$.Name}}_{{.Name}}Client, error)
{{- else}}
	{{.Name}}(*{{.InputType}}) (*{{.OutputType}}, error)
{{- end}}
{{- end}}
}
{{end}}
`

// generateProtobufCode는 ProtoFile로부터 Go 코드를 생성한다.
func generateProtobufCode(pf *ProtoFile) string {
	funcMap := template.FuncMap{
		"toLower": strings.ToLower,
		"goFieldName": func(name string) string {
			parts := strings.Split(name, "_")
			result := ""
			for _, p := range parts {
				if len(p) > 0 {
					result += strings.ToUpper(p[:1]) + p[1:]
				}
			}
			return result
		},
		"goType": protoTypeToGoType,
		"protoWireType": func(ft ProtoFieldType) string {
			switch ft {
			case ProtoString, ProtoBytes:
				return "bytes"
			case ProtoInt32, ProtoInt64, ProtoUint32, ProtoUint64, ProtoBool, ProtoEnum:
				return "varint"
			default:
				return "bytes"
			}
		},
	}

	tmpl, err := template.New("proto").Funcs(funcMap).Parse(GoStructTemplate)
	if err != nil {
		panic(fmt.Sprintf("template parse error: %v", err))
	}

	data := struct {
		Package  string
		Name     string
		Messages []ProtoMessageDef
		Enums    []ProtoEnumDef
		Services []ProtoService
	}{
		Package:  pf.Package,
		Name:     pf.Package,
		Messages: pf.Messages,
		Enums:    pf.Enums,
		Services: pf.Services,
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, data); err != nil {
		panic(fmt.Sprintf("template execute error: %v", err))
	}
	return buf.String()
}

