// Cilium의 Makefile 빌드 타겟 의존성을 위상 정렬하여 빌드 순서를 결정한다.

// BuildTarget은 Makefile의 빌드 타겟을 나타낸다.
type BuildTarget struct {
	Name         string
	Dependencies []string
	Description  string
	Commands     []string
	Phony        bool
}

// BuildGraph는 빌드 의존성 그래프를 나타낸다.
type BuildGraph struct {
	Targets map[string]*BuildTarget
}

// NewBuildGraph는 새 빌드 그래프를 생성한다.
func NewBuildGraph() *BuildGraph {
	return &BuildGraph{
		Targets: make(map[string]*BuildTarget),
	}
}

// AddTarget은 빌드 타겟을 추가한다.
func (g *BuildGraph) AddTarget(t *BuildTarget) {
	g.Targets[t.Name] = t
}

// TopologicalSort는 위상 정렬하여 빌드 순서를 반환한다.
func (g *BuildGraph) TopologicalSort() ([]string, error) {
	visited := make(map[string]bool)
	visiting := make(map[string]bool)
	var order []string

	var visit func(name string) error
	visit = func(name string) error {
		if visited[name] {
			return nil
		}
		if visiting[name] {
			return fmt.Errorf("cycle detected at target: %s", name)
		}

		visiting[name] = true

		if target, ok := g.Targets[name]; ok {
			for _, dep := range target.Dependencies {
				if err := visit(dep); err != nil {
					return err
				}
			}
		}

		visiting[name] = false
		visited[name] = true
		order = append(order, name)
		return nil
	}

	// 모든 타겟에 대해 방문
	names := make([]string, 0, len(g.Targets))
	for name := range g.Targets {
		names = append(names, name)
	}
	sort.Strings(names)

	for _, name := range names {
		if err := visit(name); err != nil {
			return nil, err
		}
	}

	return order, nil
}

// ResolveBuild는 특정 타겟을 빌드하기 위해 필요한 타겟 목록을 순서대로 반환한다.
func (g *BuildGraph) ResolveBuild(target string) ([]string, error) {
	visited := make(map[string]bool)
	var order []string

	var visit func(name string) error
	visit = func(name string) error {
		if visited[name] {
			return nil
		}
		visited[name] = true

		if t, ok := g.Targets[name]; ok {
			for _, dep := range t.Dependencies {
				if err := visit(dep); err != nil {
					return err
				}
			}
		}

		order = append(order, name)
		return nil
	}

	if err := visit(target); err != nil {
		return nil, err
	}
	return order, nil
}

// PrintDependencyTree는 의존성 트리를 시각화한다.
func (g *BuildGraph) PrintDependencyTree(target string, indent int, visited map[string]bool) {
	if visited == nil {
		visited = make(map[string]bool)
	}
	prefix := strings.Repeat("  ", indent)

	t, ok := g.Targets[target]
	if !ok {
		fmt.Printf("%s%s (external)\n", prefix, target)
		return
	}

	marker := ""
	if visited[target] {
		fmt.Printf("%s%s (already shown)\n", prefix, target)
		return
	}
	visited[target] = true

	if t.Phony {
		marker = " [PHONY]"
	}
	fmt.Printf("%s%s%s - %s\n", prefix, target, marker, t.Description)

	for _, dep := range t.Dependencies {
		g.PrintDependencyTree(dep, indent+1, visited)
	}
}

