// Cilium의 controller-gen이 Go 구조체로부터 CRD YAML을 생성하는 과정을
// 시뮬레이션한다.

// CRDField는 CRD 스키마에 들어갈 필드 정보를 나타낸다.
type CRDField struct {
	Name        string
	Type        string // string, integer, boolean, object, array
	Description string
	Required    bool
	Format      string // cidr, date-time 등
	Items       *CRDField // array일 때 원소 스키마
	Properties  []CRDField // object일 때 하위 필드
}

// CRDSpec은 CRD 생성에 필요한 정보를 나타낸다.
type CRDSpec struct {
	Group        string
	Version      string
	Kind         string
	Plural       string
	Singular     string
	ShortNames   []string
	Scope        string // Namespaced, Cluster
	Categories   []string
	PrintColumns []CRDPrintColumn
	Fields       []CRDField
}

// CRDPrintColumn은 kubectl get 출력에 표시할 열을 나타낸다.
type CRDPrintColumn struct {
	Name        string
	Type        string
	JSONPath    string
	Description string
	Priority    int
}

