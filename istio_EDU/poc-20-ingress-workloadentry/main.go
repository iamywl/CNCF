// --- Kubernetes Ingress 모델 ---

type IngressControllerMode int

const (
	ModeOFF     IngressControllerMode = iota
	ModeSTRICT
	ModeDEFAULT
)

type MeshConfig struct {
	IngressControllerMode IngressControllerMode
	IngressClass          string
	IngressService        string
	IngressSelector       string
}

type IngressTLS struct {
	Hosts      []string
	SecretName string
}

type IngressPath struct {
	Path     string
	PathType string // "Exact", "Prefix"
	Backend  IngressBackend
}

type IngressBackend struct {
	ServiceName string
	ServicePort int
}

type IngressRule struct {
	Host  string
	Paths []IngressPath
}

type Ingress struct {
	Name             string
	Namespace        string
	IngressClassName *string
	Annotations      map[string]string
	TLS              []IngressTLS
	Rules            []IngressRule
	CreatedAt        time.Time
}

// --- Istio 내부 모델 ---

type Gateway struct {
	Name      string
	Namespace string
	Selector  map[string]string
	Servers   []GatewayServer
}

type GatewayServer struct {
	Port     GatewayPort
	Hosts    []string
	TLSMode  string
	CredName string
}

type GatewayPort struct {
	Number   int
	Protocol string
	Name     string
}

type VirtualService struct {
	Name     string
	Namespace string
	Hosts    []string
	Gateways []string
	Routes   []HTTPRoute
}

type HTTPRoute struct {
	MatchURI    string
	MatchType   string // "exact", "prefix"
	Destination string
	Port        int
	Weight      int
}

// --- Ingress → Gateway + VirtualService 변환 ---

// shouldProcessIngress는 Ingress가 Istio에서 처리해야 하는지 결정한다.
// 실제: pilot/pkg/config/kube/ingress/virtualservices.go의 shouldProcessIngressWithClass
func shouldProcessIngress(mesh MeshConfig, ing Ingress) bool {
	classAnnotation, hasClass := ing.Annotations["kubernetes.io/ingress.class"]
	if hasClass {
		switch mesh.IngressControllerMode {
		case ModeOFF:
			return false
		case ModeSTRICT, ModeDEFAULT:
			return classAnnotation == mesh.IngressClass
		}
	}
	// 어노테이션 없음
	switch mesh.IngressControllerMode {
	case ModeOFF, ModeSTRICT:
		return false
	case ModeDEFAULT:
		return true
	}
	return false
}

// convertToGateway는 Ingress를 Istio Gateway로 변환한다.
// 실제: pilot/pkg/config/kube/ingress/gateways.go의 convertIngressV1alpha3
func convertToGateway(ing Ingress, mesh MeshConfig, domainSuffix string) Gateway {
	gw := Gateway{
		Name:      fmt.Sprintf("%s-istio-autogenerated-k8s-ingress-%s", ing.Name, ing.Namespace),
		Namespace: "istio-system",
		Selector:  getSelector(mesh.IngressSelector, mesh.IngressService),
	}

	for i, tls := range ing.TLS {
		if tls.SecretName == "" {
			continue
		}
		hosts := tls.Hosts
		if len(hosts) == 0 {
			hosts = []string{"*"}
		}
		gw.Servers = append(gw.Servers, GatewayServer{
			Port: GatewayPort{
				Number:   443,
				Protocol: "HTTPS",
				Name:     fmt.Sprintf("https-443-ingress-%s-%s-%d", ing.Name, ing.Namespace, i),
			},
			Hosts:    hosts,
			TLSMode:  "SIMPLE",
			CredName: tls.SecretName,
		})
	}

	gw.Servers = append(gw.Servers, GatewayServer{
		Port: GatewayPort{
			Number:   80,
			Protocol: "HTTP",
			Name:     fmt.Sprintf("http-80-ingress-%s-%s", ing.Name, ing.Namespace),
		},
		Hosts: []string{"*"},
	})

	return gw
}

func getSelector(ingressSelector, ingressService string) map[string]string {
	if ingressSelector != "" {
		return map[string]string{"istio": ingressSelector}
	}
	if ingressService != "" && ingressService != "istio-ingressgateway" {
		return map[string]string{"istio": ingressService}
	}
	return map[string]string{"istio": "ingressgateway"}
}

// IngressRuleEntry는 개별 규칙을 나타낸다.
type IngressRuleEntry struct {
	IngressName string
	Namespace   string
	RuleIndex   int
	CreatedAt   time.Time
	Host        string
	Paths       []IngressPath
}

// buildRuleCollection은 Ingress 규칙을 호스트별로 그룹핑한다.
// 실제: pilot/pkg/config/kube/ingress/ingress.go의 RuleCollection
func buildRuleCollection(ingresses []Ingress) map[string][]IngressRuleEntry {
	hostRules := make(map[string][]IngressRuleEntry)
	for _, ing := range ingresses {
		for idx, rule := range ing.Rules {
			if len(rule.Paths) == 0 {
				continue
			}
			host := rule.Host
			if host == "" {
				host = "*"
			}
			entry := IngressRuleEntry{
				IngressName: ing.Name,
				Namespace:   ing.Namespace,
				RuleIndex:   idx,
				CreatedAt:   ing.CreatedAt,
				Host:        host,
				Paths:       rule.Paths,
			}
			hostRules[host] = append(hostRules[host], entry)
		}
	}

	// 생성 시간순 정렬
	for host := range hostRules {
		rules := hostRules[host]
		sort.SliceStable(rules, func(i, j int) bool {
			return rules[i].CreatedAt.Before(rules[j].CreatedAt)
		})
		hostRules[host] = rules
	}
	return hostRules
}

// convertToVirtualServices는 호스트별 규칙을 VirtualService로 변환한다.
// 실제: pilot/pkg/config/kube/ingress/virtualservices.go의 VirtualServices
func convertToVirtualServices(hostRules map[string][]IngressRuleEntry, domainSuffix string) []VirtualService {
	var result []VirtualService
	for host, rules := range hostRules {
		if len(rules) == 0 {
			continue
		}
		var httpRoutes []HTTPRoute
		for _, rule := range rules {
			for _, path := range rule.Paths {
				route := HTTPRoute{
					MatchURI:    path.Path,
					MatchType:   strings.ToLower(path.PathType),
					Destination: fmt.Sprintf("%s.%s.svc.%s", path.Backend.ServiceName, rule.Namespace, domainSuffix),
					Port:        path.Backend.ServicePort,
					Weight:      100,
				}
				httpRoutes = append(httpRoutes, route)
			}
		}
		// 경로 정렬: 긴 경로 우선, 같은 길이면 Exact > Prefix
		sort.SliceStable(httpRoutes, func(i, j int) bool {
			li, lj := len(httpRoutes[i].MatchURI), len(httpRoutes[j].MatchURI)
			if li == lj {
				return httpRoutes[i].MatchType == "exact" && httpRoutes[j].MatchType != "exact"
			}
			return li > lj
		})

		gwName := fmt.Sprintf("istio-system/%s-istio-autogenerated-k8s-ingress-%s",
			rules[0].IngressName, rules[0].Namespace)

		vs := VirtualService{
			Name:      fmt.Sprintf("%s-%s-istio-autogenerated-k8s-ingress", strings.ReplaceAll(host, ".", "-"), rules[0].IngressName),
			Namespace: rules[0].Namespace,
			Hosts:     []string{host},
			Gateways:  []string{gwName},
			Routes:    httpRoutes,
		}
		result = append(result, vs)
	}
	return result
}

// --- WorkloadEntry 자동 등록 ---

type WorkloadEntry struct {
	Name        string
	Namespace   string
	Address     string
	Labels      map[string]string
	Network     string
	Locality    string
	Annotations map[string]string
}

type WorkloadGroup struct {
	Name      string
	Namespace string
	Labels    map[string]string
	Template  WorkloadEntry
}

// ProxyMeta는 VM 프록시 메타데이터를 나타낸다.
type ProxyMeta struct {
	ID                string
	IPAddress         string
	Namespace         string
	AutoRegisterGroup string
	Network           string
	ServiceAccount    string
}

// Connection은 xDS 연결을 나타낸다.
type Connection struct {
	ID          string
	Proxy       ProxyMeta
	ConnectedAt time.Time
}

// AdsConnections는 프록시별 활성 연결을 추적한다.
// 실제: pilot/pkg/autoregistration/connections.go
type AdsConnections struct {
	mu      sync.Mutex
	byProxy map[string]map[string]*Connection // proxyKey → connID → connection
}

func NewAdsConnections() *AdsConnections {
	return &AdsConnections{
		byProxy: make(map[string]map[string]*Connection),
	}
}

func proxyKey(p ProxyMeta) string {
	return fmt.Sprintf("%s/%s/%s/%s", p.Network, p.IPAddress, p.AutoRegisterGroup, p.Namespace)
}

func (ac *AdsConnections) Connect(conn *Connection) {
	ac.mu.Lock()
	defer ac.mu.Unlock()
	key := proxyKey(conn.Proxy)
	if ac.byProxy[key] == nil {
		ac.byProxy[key] = make(map[string]*Connection)
	}
	ac.byProxy[key][conn.ID] = conn
}

// Disconnect는 연결을 해제하고 남은 연결이 있는지 반환한다.
func (ac *AdsConnections) Disconnect(conn *Connection) bool {
	ac.mu.Lock()
	defer ac.mu.Unlock()
	key := proxyKey(conn.Proxy)
	conns := ac.byProxy[key]
	if conns == nil {
		return false
	}
	delete(conns, conn.ID)
	if len(conns) == 0 {
		delete(ac.byProxy, key)
		return false // 더 이상 연결 없음
	}
	return true // 아직 다른 연결 있음
}

// AutoRegistrationController는 VM 워크로드 자동 등록을 관리한다.
// 실제: pilot/pkg/autoregistration/controller.go
type AutoRegistrationController struct {
	mu             sync.Mutex
	instanceID     string
	workloadEntries map[string]*WorkloadEntry
	workloadGroups  map[string]*WorkloadGroup
	connections     *AdsConnections
	cleanupDelay   time.Duration
}

func NewAutoRegistrationController(instanceID string) *AutoRegistrationController {
	return &AutoRegistrationController{
		instanceID:      instanceID,
		workloadEntries: make(map[string]*WorkloadEntry),
		workloadGroups:  make(map[string]*WorkloadGroup),
		connections:     NewAdsConnections(),
		cleanupDelay:    200 * time.Millisecond,
	}
}

func (arc *AutoRegistrationController) AddWorkloadGroup(wg *WorkloadGroup) {
	arc.mu.Lock()
	defer arc.mu.Unlock()
	key := wg.Namespace + "/" + wg.Name
	arc.workloadGroups[key] = wg
}

// OnConnect는 프록시 연결 시 WorkloadEntry를 생성/갱신한다.
func (arc *AutoRegistrationController) OnConnect(conn *Connection) error {
	arc.mu.Lock()
	defer arc.mu.Unlock()

	proxy := conn.Proxy
	if proxy.AutoRegisterGroup == "" {
		return nil
	}

	entryName := autoRegisteredName(proxy)
	arc.connections.Connect(conn)

	// 기존 WLE 확인
	wleKey := proxy.Namespace + "/" + entryName
	if wle, exists := arc.workloadEntries[wleKey]; exists {
		// 연결 상태 갱신
		wle.Annotations["istio.io/connectedAt"] = conn.ConnectedAt.Format(time.RFC3339Nano)
		wle.Annotations["istio.io/workloadController"] = arc.instanceID
		delete(wle.Annotations, "istio.io/disconnectedAt")
		fmt.Printf("  [AutoReg] WorkloadEntry 갱신: %s (재접속)\n", wleKey)
		return nil
	}

	// WorkloadGroup 조회
	wgKey := proxy.Namespace + "/" + proxy.AutoRegisterGroup
	wg, ok := arc.workloadGroups[wgKey]
	if !ok {
		return fmt.Errorf("WorkloadGroup %s not found", wgKey)
	}

	// WorkloadEntry 생성
	wle := &WorkloadEntry{
		Name:      entryName,
		Namespace: proxy.Namespace,
		Address:   proxy.IPAddress,
		Labels:    mergeLabels(wg.Template.Labels, wg.Labels),
		Network:   proxy.Network,
		Annotations: map[string]string{
			"istio.io/autoRegistrationGroup": wg.Name,
			"istio.io/connectedAt":           conn.ConnectedAt.Format(time.RFC3339Nano),
			"istio.io/workloadController":    arc.instanceID,
		},
	}
	arc.workloadEntries[wleKey] = wle
	fmt.Printf("  [AutoReg] WorkloadEntry 생성: %s (addr=%s, network=%s)\n",
		wleKey, wle.Address, wle.Network)
	return nil
}

// OnDisconnect는 프록시 연결 해제 시 정리를 예약한다.
func (arc *AutoRegistrationController) OnDisconnect(conn *Connection) {
	proxy := conn.Proxy
	if proxy.AutoRegisterGroup == "" {
		return
	}

	// 다른 연결이 남아있는지 확인
	remaining := arc.connections.Disconnect(conn)
	if remaining {
		fmt.Printf("  [AutoReg] 연결 해제 (다른 연결 남아있음, 정리 안 함): %s\n", conn.ID)
		return
	}

	entryName := autoRegisteredName(proxy)
	wleKey := proxy.Namespace + "/" + entryName

	arc.mu.Lock()
	wle, exists := arc.workloadEntries[wleKey]
	if exists {
		wle.Annotations["istio.io/disconnectedAt"] = time.Now().Format(time.RFC3339Nano)
		delete(wle.Annotations, "istio.io/connectedAt")
		fmt.Printf("  [AutoReg] 연결 해제: %s (유예 기간 시작)\n", wleKey)
	}
	arc.mu.Unlock()

	// 유예 기간 후 정리
	go func() {
		time.Sleep(arc.cleanupDelay)
		arc.mu.Lock()
		defer arc.mu.Unlock()
		wle, exists := arc.workloadEntries[wleKey]
		if !exists {
			return
		}
		// 재접속했는지 확인
		if _, connected := wle.Annotations["istio.io/connectedAt"]; connected {
			fmt.Printf("  [AutoReg] 정리 취소 (재접속됨): %s\n", wleKey)
			return
		}
		delete(arc.workloadEntries, wleKey)
		fmt.Printf("  [AutoReg] WorkloadEntry 삭제: %s\n", wleKey)
	}()
}

func autoRegisteredName(proxy ProxyMeta) string {
	name := proxy.AutoRegisterGroup + "-" + strings.ReplaceAll(proxy.IPAddress, ":", "-")
	if proxy.Network != "" {
		name += "-" + proxy.Network
	}
	if len(name) > 253 {
		name = name[len(name)-253:]
	}
	return name
}

func mergeLabels(base, override map[string]string) map[string]string {
	result := make(map[string]string)
	for k, v := range base {
		result[k] = v
	}
	for k, v := range override {
		result[k] = v
	}
	return result
}

