// Istio Ingress 호환 + WorkloadEntry 자동 등록 시뮬레이션
//
// 실제 소스 참조:
//   - pilot/pkg/config/kube/ingress/controller.go     → Ingress Controller
//   - pilot/pkg/config/kube/ingress/ingress.go        → SupportedIngresses, RuleCollection
//   - pilot/pkg/config/kube/ingress/virtualservices.go → VirtualService 변환
//   - pilot/pkg/config/kube/ingress/gateways.go       → Gateway 변환
//   - pilot/pkg/autoregistration/controller.go         → AutoRegistration
//   - pilot/pkg/autoregistration/connections.go        → adsConnections
//
// 핵심 알고리즘:
//   1. K8s Ingress → SupportedIngresses 필터 → RuleCollection → VirtualService + Gateway
//   2. VM xDS 연결 → WorkloadEntry 자동 생성 → 연결 해제 시 유예 기간 후 삭제
//   3. ADS 연결 추적: 다중 연결 지원, 모든 연결 해제 시에만 정리
